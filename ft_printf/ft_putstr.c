#include "ft_printf.h"

int	ft_putstr(char *c)
{
	int	count;

	count = 0;
	if (!c)
	{
		return (write(1, "(null)", 6));
	}
	while (c[count] != '\0')
	{
		write(1, &c[count], 1);
		count++;
	}
	return (count);
}
